package com.soma;

public class Calcular {

	public int realiazaSoma(int numero1, int numero2) {
		return (numero1 + numero2);
	}

	public int realiazaMultiplicacao(int numero1, int numero2) {
		return (numero1 * numero2);

	}

	public int realizaSubtracao(int numero1, int numero2) {
		return (numero1 - numero2);

	}

}
